package calculadora.operacoes;

public class Divisao {

    //fazendo a divisao
    public double divisaoRessultado (double num1, double num2) {
        double resultado = num1 / num2;
        return resultado;
    }

    //fazendo a divisao e retornando o resto dela
    public double divisaoResto (double num1, double num2) {
        double resultado = num1 % num2;
        return resultado;
    }
}
