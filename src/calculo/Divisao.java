package calculo;

public class Divisao {
    private int num1;
    private int num2;

    public int divisaoRessultado (int num1, int num2) {
        int resultado = num1 / num2;
        return resultado;
    }

    public int divisaoResto (int num1, int num2) {
        int resultado = num1 % num2;
        return resultado;
    }
}
